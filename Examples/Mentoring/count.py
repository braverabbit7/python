# Вывод на несколько строк тройные кавычки
print("""fdaffafwa
afdwafaf
afawafa""")
# Перевод на след строку \n
print("Привет \n мир")
#\t табуляция
print("\t привет \n \t мир")
#Печать с""
print("'привет'")

#Экранирование
print("Привет \"в\" кавычках")

#Сложение строк (конкостинация)
a = "привет"
b = "мир"
print(a + " " + b)

#Вывод определенного символа
print(a[0])
#Срез
print(b[0:2])
